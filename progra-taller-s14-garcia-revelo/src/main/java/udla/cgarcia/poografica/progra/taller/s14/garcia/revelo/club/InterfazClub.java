    private void afiliarSocioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_afiliarSocioButtonActionPerformed
            afiliarSocioButton.setSelected(false); // Soltar botón
            try {
                String cedula = JOptionPane.showInputDialog(this, "Ingrese Cédula:");
                if(cedula == null) return;

                String nombre = JOptionPane.showInputDialog(this, "Ingrese Nombre:");
                if(nombre == null) return;

                String[] opts = {"Regular", "VIP"};
                int tipoSel = JOptionPane.showOptionDialog(this, "Tipo Socio", "Tipo", 
                        JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, opts, opts[0]);

                Tipo tipo = (tipoSel == 1) ? Tipo.VIP : Tipo.REGULAR;

                club.afiliarSocio(cedula, nombre, tipo);
                mostrarInfo.setText("Socio " + nombre + " afiliado con éxito.");
                JOptionPane.showMessageDialog(this, "Socio afiliado correctamente.");

            } catch (HeadlessException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            } catch (Exception ex) {
                System.getLogger(InterfazClub.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
    }//GEN-LAST:event_afiliarSocioButtonActionPerformed

