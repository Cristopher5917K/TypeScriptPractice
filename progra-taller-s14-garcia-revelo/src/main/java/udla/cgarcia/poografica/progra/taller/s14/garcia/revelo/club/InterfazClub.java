    private void pagarFacturaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pagarFacturaButtonActionPerformed
        pagarFacturaButton.setSelected(false);
        try {
            String cedula = JOptionPane.showInputDialog(this, "Cédula del Socio:");
            if(cedula == null) return;
            
            ArrayList<Factura> facts = club.darFacturasSocio(cedula);
            if(facts.isEmpty()) {
                mostrarInfo.setText("No hay facturas pendientes.");
                JOptionPane.showMessageDialog(this, "El socio no tiene facturas pendientes.");
                return;
            }
            
            // Crear lista para el popup
            String[] lista = new String[facts.size()];
            for(int i=0; i<facts.size(); i++) {
                lista[i] = i + ". " + facts.get(i).toString();
            }
            
            String sel = (String) JOptionPane.showInputDialog(this, "Seleccione Factura", "Pagar",
                    JOptionPane.QUESTION_MESSAGE, null, lista, lista[0]);
            
            if(sel != null) {
                int indice = Integer.parseInt(sel.split("\\.")[0]);
                club.pagarFacturaSocio(cedula, indice);
                mostrarInfo.setText("Factura pagada exitosamente.");
                JOptionPane.showMessageDialog(this, "Factura pagada con éxito.");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_pagarFacturaButtonActionPerformed

